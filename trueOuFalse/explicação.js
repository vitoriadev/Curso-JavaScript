const numero1 = 5;
const numero2 = "5";

// compara se os valores são iguais, ou seja, se 5 (number)
// é igual a "5" (string);
console.log(numero1 == numero2); // true

// compara apenas se os valores são diferente;
console.log(numero1 != numero2); // false

// compara se o valor e o tipo são iguais, ou seja, o tipo
// number não é igual ao tipo string;
console.log(numero1 === numero2); // false

// compara se o valor e o tipo são diferentes, ou seja, o tipo
// number é diferente do tipo string;
console.log(numero1 !== numero2); //true
